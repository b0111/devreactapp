async function loginUser(credentials) {
  return fetch('http://localhost:4001/login', {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json'
    },
    body: JSON.stringify(credentials)
  })
    .then(data => data.json())
 }


// TODO remove, this demo shouldn't need to reset the theme.

const defaultTheme = createTheme();

function Login(){
const navigate = useNavigate();
const [userEmail, setUserName] = useState();
const [password, setPassword] = useState();

const [authenticated, setauthenticated] = useRecoilState(checkLoginAtom);


const handleSubmit = async e => {
    e.preventDefault();
    const response = await loginUser({
      userEmail,
      password
    });
    if ('accessToken' in response) {
        setauthenticated('true')
        localStorage.setItem("authenticated", true);


      swal("Success", response.message, "success", {
        buttons: false,
        timer: 2000,
      })
      .then((value) => {
        localStorage.setItem('accessToken', response['accessToken']);
        localStorage.setItem('user', JSON.stringify(response['user']));
        window.location.href = "/profile";
      });
    } else {
    const err =  JSON.stringify(response.errors,false);
      console.log(err[0]);
          swal("Failed", 'test', "error", {
        dangerMode: true,
         buttons: true,
        }  );

    }
  }

  useEffect(() => {

    const loggedInUser = localStorage.getItem("authenticated");
    if (loggedInUser) {
      setauthenticated(loggedInUser);
    }
  }, []);

  if (authenticated == 'false') {
    return (
      <ThemeProvider theme={defaultTheme}>
      <Container component="main" maxWidth="xs">
        <CssBaseline />
        <Box
          sx={{
            marginTop: 8,
            display: 'flex',
            flexDirection: 'column',
            alignItems: 'center',
          }}
        >
          <Avatar sx={{ m: 1, bgcolor: 'secondary.main' }}>
            {/* <LockOutlinedIcon /> */}
          </Avatar>
          <Typography component="h1" variant="h5">
            Sign in
          </Typography>
          <Box component="form" onSubmit={handleSubmit} noValidate sx={{ mt: 1 }}>
            <TextField
              margin="normal"
              required
              fullWidth
              id="email"
              label="Email Address"
              name="email"
              autoComplete="email"
              autoFocus   onChange={e => setUserName(e.target.value)}
            />
            <TextField
              margin="normal"
              required
              fullWidth
              name="password"
              label="Password"
              type="password"
              id="password"
              autoComplete="current-password"  onChange={e => setPassword(e.target.value)}
            />
            <FormControlLabel
              control={<Checkbox value="remember" color="primary" />}
              label="Remember me"
            />
            <Button
              type="submit"
              fullWidth
              variant="contained"
              sx={{ mt: 3, mb: 2 }}
            >
              Sign In
            </Button>
            <Grid container>
              <Grid item xs>
                <Link href="#" variant="body2">
                  Forgot password?
                </Link>
              </Grid>
              <Grid item>
                <Link href="#" variant="body2">
                  {"Don't have an account? Sign Up"}
                </Link>
              </Grid>
            </Grid>
          </Box>
        </Box>
        <br/><br/><br/>
     
      </Container>
    </ThemeProvider>
    )

  }else{
    navigate('/profile');
  }
  
}


export  default Login;